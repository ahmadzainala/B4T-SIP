class Form_model extends CI_Model
{

    public $table = 'form';
    public $id = 'id_form';
    public $order = 'DESC';

    function __construct()
    {
        parent::__construct();
    }

    // get all
    function get_all()
    {
        $this->db->order_by($this->id, $this->order);
        return $this->db->get($this->table)->result();
    }

    function get_all_detail()
    {
        return $this->db->query('select * from user_akun a, form b where a.id_user = b.id_user')->result();
    }

    // get data by id
    function get_by_id($id)
    {
        $this->db->where($this->id, $id);
        return $this->db->get($this->table)->row();
    }

    //get by id user
    function get_by_user($id)
    {
        $this->db->where('id_user', $id);
        return $this->db->get($this->table)->result();
    }

    //get by id user
    function get_by_user_now($id,$date)
    {
        $this->db->where('id_user', $id);
        $this->db->where('date', $date);
        $this->db->order_by('id_form','desc');
        return $this->db->get($this->table)->row();
    }

    
    // get total rows
    function total_rows($q = NULL) {
        $this->db->like('id_form', $q);
	$this->db->or_like('id_user', $q);
	$this->db->or_like('date', $q);
	$this->db->or_like('information', $q);
	$this->db->or_like('date_needs', $q);
	$this->db->or_like('that', $q);
	$this->db->from($this->table);
        return $this->db->count_all_results();
    }

    // get data with limit and search
    function get_limit_data($limit, $start = 0, $q = NULL) {
        $this->db->order_by($this->id, $this->order);
        $this->db->like('id_form', $q);
	$this->db->or_like('id_user', $q);
	$this->db->or_like('date', $q);
	$this->db->or_like('information', $q);
	$this->db->or_like('date_needs', $q);
	$this->db->or_like('that', $q);
	$this->db->limit($limit, $start);
        return $this->db->get($this->table)->result();
    }

    // insert data
    function insert($data)
    {
        $this->db->insert($this->table, $data);
    }

    // update data
    function update($id, $data)
    {
        $this->db->where($this->id, $id);
        $this->db->update($this->table, $data);
    }

    // delete data
    function delete($id)
    {
        $this->db->where($this->id, $id);
        $this->db->delete($this->table);
    }

}

